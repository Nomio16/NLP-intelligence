    writer = csv.writer(f)
    writer.writerow(["ID", "Text", "Source"])
    for i in range(1, 501):
        template = random.choice(templates)
        text = template.format(
            pct=random.randint(3, 45),
            amount=random.choice([1.5, 2.5, 3.2, 5, 10, 20, 50, 100]),
            num=random.randint(2, 5000),
            org=random.choice(orgs),
            aimag=random.choice(aimags)
        )
        # Modify some words slightly to add noise to text so it's not strictly exact matching
